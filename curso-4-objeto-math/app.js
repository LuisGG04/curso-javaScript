// Devuelve -1 en negativo, 1 en positivo y 0 cuando es 0

console.log(Math.round(Math.random()*100));

// Para números al azar pero con un mínimo y un máximo se utiliza la siguiente formula:

// console.log(Math.random() * (max - min)+min;

